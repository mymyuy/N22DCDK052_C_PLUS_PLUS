#include <iostream>
using namespace std;

//chuong trinh chinh
int main()
{
    int floors, rooms, suites; //khai bao 3 bien kieu so nguyen
    floors=15; // gan 15 cho floors
    rooms=300; // gan 300 cho rooms
    suites=30; // gan 30 cho suites
    cout << "The Grande Hotel has " << floors << " floors\n";
    cout << "with " << rooms << " rooms and " << suites;
    cout << " suites.\n";
    /* Output:
    The Grande Hotel has 15 floors
    with 300 rooms and 30 suites.
    */
    return 0;
}